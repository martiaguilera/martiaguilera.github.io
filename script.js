// ===== LANGUAGE =====
function changeLanguage(lang) {
  localStorage.setItem('preferredLanguage', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-lang]').forEach(el => {
    if (el.classList.contains('typing-text')) return;
    const key = el.getAttribute('data-lang');
    const val = translations[lang]?.[key];
    if (!val) return;
    el.innerHTML = val.includes('<') ? val : '';
    if (!val.includes('<')) el.textContent = val;
  });

  startTypingEffect();

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

// ===== TYPING EFFECT =====
let typingTimer = null;
let typingSession = null;

function startTypingEffect() {
  const el = document.querySelector('.typing-text');
  if (!el) return;
  const lang = localStorage.getItem('preferredLanguage') || 'ca';
  const text = translations[lang]?.[el.getAttribute('data-lang')] || '';
  if (!text) return;

  if (typingTimer) clearTimeout(typingTimer);
  const session = {};
  typingSession = session;
  el.textContent = '';
  el.classList.remove('complete');

  let i = 0;
  function type() {
    if (typingSession !== session) return;
    if (i < text.length) {
      el.textContent += text[i++];
      typingTimer = setTimeout(type, 42);
    } else {
      el.classList.add('complete');
    }
  }
  typingTimer = setTimeout(type, 600);
}

// ===== INTERSECTION OBSERVER (scroll animations) =====
const io = new IntersectionObserver((entries) => {
  entries.forEach((entry, idx) => {
    if (entry.isIntersecting) {
      setTimeout(() => entry.target.classList.add('visible'), idx * 80);
      io.unobserve(entry.target);
    }
  });
}, { threshold: 0.12, rootMargin: '0px 0px -60px 0px' });

// ===== NAVBAR SCROLL =====
function handleNavbarScroll() {
  const navbar = document.getElementById('navbar');
  if (!navbar) return;
  navbar.classList.toggle('scrolled', window.scrollY > 40);
}

// ===== ACTIVE NAV LINK =====
function updateActiveLink() {
  const sections = document.querySelectorAll('section[id]');
  const navLinks = document.querySelectorAll('.nav-link');
  sections.forEach(section => {
    const rect = section.getBoundingClientRect();
    if (rect.top <= 90 && rect.bottom >= 90) {
      navLinks.forEach(link => {
        link.classList.toggle('active', link.getAttribute('href') === `#${section.id}`);
      });
    }
  });
}

// ===== INIT =====
document.addEventListener('DOMContentLoaded', () => {
  // Language
  const savedLang = localStorage.getItem('preferredLanguage') || 'ca';
  changeLanguage(savedLang);

  // Fade-up elements
  document.querySelectorAll('.fade-up').forEach(el => io.observe(el));

  // Hamburger
  const hamburger = document.getElementById('hamburger-menu');
  const mobileMenu = document.getElementById('mobile-menu');
  hamburger?.addEventListener('click', () => {
    hamburger.classList.toggle('active');
    mobileMenu.classList.toggle('active');
  });

  // Close mobile menu on link click
  document.querySelectorAll('.mobile-menu .nav-link').forEach(link => {
    link.addEventListener('click', () => {
      hamburger?.classList.remove('active');
      mobileMenu?.classList.remove('active');
    });
  });

  // Smooth scroll
  document.querySelectorAll('.nav-link[href^="#"]').forEach(link => {
    link.addEventListener('click', e => {
      e.preventDefault();
      const target = document.querySelector(link.getAttribute('href'));
      if (target) {
        window.scrollTo({ top: target.offsetTop - 72, behavior: 'smooth' });
      }
    });
  });

  // Hero button
  document.querySelector('.hero-btn')?.addEventListener('click', e => {
    e.preventDefault();
    const target = document.querySelector('#work');
    if (target) window.scrollTo({ top: target.offsetTop - 72, behavior: 'smooth' });
  });

  // Language buttons (both desktop and mobile)
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      changeLanguage(btn.getAttribute('data-lang'));
    });
  });

  // Scroll events
  window.addEventListener('scroll', () => {
    handleNavbarScroll();
    updateActiveLink();
  }, { passive: true });

  handleNavbarScroll();
  updateActiveLink();
});
